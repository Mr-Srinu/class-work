class Arithmetic
{
	public static void main(String args[])
	{
		System.out.println("--Arithmetic operators--");
		System.out.println(" 10 + 5 ="+(10+5));
		System.out.println(" 10 - 5 ="+(10-5));
		System.out.println(" 10 * 5 ="+(10*5));
		System.out.println(" 10 / 5 ="+(10/5));
		System.out.println(" 10 % 4 ="+(10%4));
		
	}
}