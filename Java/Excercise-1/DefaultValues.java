import java.io.*;
class DefaultValues
{
	static int a;
	static float b;
	static double c;
	static String s;
	static long d;
	static short e;
	static boolean f;
	public static void main(String args[])
	{		
		System.out.println("Default value of int type :"+a);
		System.out.println("Default value of float type :"+b);
		System.out.println("Default value of double type :"+c);
		System.out.println("Default value of String type :"+s);
		System.out.println("Default value of long type :"+d);
		System.out.println("Default value of short type :"+e);
		System.out.println("Default value of bool type :"+f);
	}
}